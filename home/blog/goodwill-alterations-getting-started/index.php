<p>I've been meaning to start working on this for awhile. Being uncompromisingly picky with clothing decisions and living off ramen wages can lead to a few painful choices. I'd really love to invest in pieces from world class designers but the cost is just unjustifiable at this point in my life. There's a few options to pursue here:</p>

<ol start="1">
  <li>Spam lowball offers on <a href="https://grailed.com">grailed.com</a>.</li>
  <li>Give up and go normcore/thriftcore etc.</li>
  <li>Make it yourself.</li>
</ol>

<p>I'm here to tell you that the first two options are shameful, confusing distractions; we can do better. My big idea is to harness the endless selection of cheap clothing and fabric at Goodwill and turn it into something interesting and wearable. I'm starting from near-zero sewing experience and this is really an experiment more than anything. We'll see how I fare.</p>

<h2>What to Get</h2>

<p>I bought my first machine secondhand for $25. Obviously it had quite a few issues or it wouldn't have been sitting in the bargain rack. The light was broken, it was missing a foot and the tension knob was locked at one grip setting. <a href="http://www.sewingpartsonline.com/singer-sewing-machine-parts-model-list.aspx">This site</a> was immensely helpful for my parts issues and seems to be comprehensive enough to cover any used machine you'd come across. I fixed the tension by pulling the cap off and cleaning between the grip plates. Thanks to the wealth of <a href="https://www.youtube.com/watch?v=og0WnNQym6A&index=1&list=PLeEMs9XJguBSu-UyFztsRI4uhUjyXsdoy">grandma-hosted Youtube series</a> available to me I was able to learn enough to get started fairly quickly. What I couldn't get secondhand I caved and bought from Jo-ann fabrics which is generally a bit pricey.</p>

